def translate(value: str, language: str) -> str:
    question = music_question(value, language)
    if question is not None:
        return question

    terms = {}
    terms.update(EXACT.get("en", {}))
    terms.update(COMMON_TERMS.get(language, {}))
    if value in terms:
        return terms[value]

    translated = value
    for source, replacement in sorted(terms.items(), key=lambda item: len(item[0]), reverse=True):
        translated = translated.replace(source, replacement)
    for source, replacement in sorted(PHRASES[language], key=lambda item: len(item[0]), reverse=True):
        translated = translated.replace(source, replacement)

    translated = translated.translate(TRANSLIT)
    translated = re.sub(r"\s+", " ", translated).strip()
    return translated


def main() -> None:
    source_path = ROOT / f"{SOURCE}.lproj" / "data.json"
    source_data = json.loads(source_path.read_text(encoding="utf-8"))

    for language in TARGETS:
        target_path = ROOT / f"{language}.lproj" / "data.json"
        target_data = json.loads(target_path.read_text(encoding="utf-8"))

        for source_theme, target_theme in zip(source_data, target_data, strict=True):
            if source_theme["id"] != target_theme["id"]:
                raise ValueError(f"Theme id mismatch: {source_theme['id']} != {target_theme['id']}")

            for source_question, target_question in zip(source_theme["questions"], target_theme["questions"], strict=True):
                target_question["question"] = (
                    translate(source_question["question"], language)
                    if has_cyrillic(source_question["question"])
                    else source_question["question"]
                )

                target_question["answers"] = [
                    translate(source_answer, language) if has_cyrillic(source_answer) else source_answer
                    for source_answer in source_question["answers"]
                ]

                target_question["correctAnswer"] = (
                    translate(source_question["correctAnswer"], language)
                    if has_cyrillic(source_question["correctAnswer"])
                    else source_question["correctAnswer"]
                )

                if target_question["correctAnswer"] not in target_question["answers"]:
                    raise ValueError(
                        f"{language} correctAnswer mismatch for {source_question['question']}: "
                        f"{target_question['correctAnswer']} not in {target_question['answers']}"
                    )

        target_path.write_text(json.dumps(target_data, ensure_ascii=False, indent=4) + "\n", encoding="utf-8")
        print(f"Updated {target_path}")


if __name__ == "__main__":
    main()
